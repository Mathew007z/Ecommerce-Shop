


// Constructor de mis Productos / Objetos.
class Producto {
    constructor(id,nombre,marca,prenda,color,stock,precio,img,cantidad){
        this.id = id;
        this.nombre = nombre;
        this.marca = marca;
        this.prenda = prenda;
        this.color = color;
        this.stock = stock;
        this.precio = precio;
        this.img = img;
        this.cantidad = cantidad;
    }
};
